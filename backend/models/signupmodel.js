const mongoose = require('mongoose');

const signupTemp = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const signUp = mongoose.model('signUp', signupTemp);

module.exports = signUp;
